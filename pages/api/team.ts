import { groupBy, keyBy, omit, pick } from 'lodash'
import path from 'path'
// see different data states in comments below functions
export default async function handler(req, res) {
    //copy and pasted column headers for ease then split all by white space so I can get an array
    const columnArray =
        'team  season name	gameId	playerTeam	opposingTeam	home_or_away	gameDate	position	situation	xGoalsPercentage	corsiPercentage	fenwickPercentage	iceTime	xOnGoalFor	xGoalsFor	xReboundsFor	xFreezeFor	xPlayStoppedFor	xPlayContinuedInZoneFor	xPlayContinuedOutsideZoneFor	flurryAdjustedxGoalsFor	scoreVenueAdjustedxGoalsFor	flurryScoreVenueAdjustedxGoalsFor	shotsOnGoalFor	missedShotsFor	blockedShotAttemptsFor	shotAttemptsFor	goalsFor	reboundsFor	reboundGoalsFor	freezeFor	playStoppedFor	playContinuedInZoneFor	playContinuedOutsideZoneFor	savedShotsOnGoalFor	savedUnblockedShotAttemptsFor	penaltiesFor	penalityMinutesFor	faceOffsWonFor	hitsFor	takeawaysFor	giveawaysFor	lowDangerShotsFor	mediumDangerShotsFor	highDangerShotsFor	lowDangerxGoalsFor	mediumDangerxGoalsFor	highDangerxGoalsFor	lowDangerGoalsFor	mediumDangerGoalsFor	highDangerGoalsFor	scoreAdjustedShotsAttemptsFor	unblockedShotAttemptsFor	scoreAdjustedUnblockedShotAttemptsFor	dZoneGiveawaysFor	xGoalsFromxReboundsOfShotsFor	xGoalsFromActualReboundsOfShotsFor	reboundxGoalsFor	totalShotCreditFor	scoreAdjustedTotalShotCreditFor	scoreFlurryAdjustedTotalShotCreditFor	xOnGoalAgainst	xGoalsAgainst	xReboundsAgainst	xFreezeAgainst	xPlayStoppedAgainst	xPlayContinuedInZoneAgainst	xPlayContinuedOutsideZoneAgainst	flurryAdjustedxGoalsAgainst	scoreVenueAdjustedxGoalsAgainst	flurryScoreVenueAdjustedxGoalsAgainst	shotsOnGoalAgainst	missedShotsAgainst	blockedShotAttemptsAgainst	shotAttemptsAgainst	goalsAgainst	reboundsAgainst	reboundGoalsAgainst	freezeAgainst	playStoppedAgainst	playContinuedInZoneAgainst	playContinuedOutsideZoneAgainst	savedShotsOnGoalAgainst	savedUnblockedShotAttemptsAgainst	penaltiesAgainst	penalityMinutesAgainst	faceOffsWonAgainst	hitsAgainst	takeawaysAgainst	giveawaysAgainst	lowDangerShotsAgainst	mediumDangerShotsAgainst	highDangerShotsAgainst	lowDangerxGoalsAgainst	mediumDangerxGoalsAgainst	highDangerxGoalsAgainst	lowDangerGoalsAgainst	mediumDangerGoalsAgainst	highDangerGoalsAgainst	scoreAdjustedShotsAttemptsAgainst	unblockedShotAttemptsAgainst	scoreAdjustedUnblockedShotAttemptsAgainst	dZoneGiveawaysAgainst	xGoalsFromxReboundsOfShotsAgainst	xGoalsFromActualReboundsOfShotsAgainst	reboundxGoalsAgainst	totalShotCreditAgainst	scoreAdjustedTotalShotCreditAgainst	scoreFlurryAdjustedTotalShotCreditAgainst'.split(
            /\s+/
        )

    // Get some nice auto complete on the columns as the keys
    type ColumnHeaderForTeams = { [C in typeof columnArray[number]]: number }

    const columnHeaderMap: ColumnHeaderForTeams = columnArray.reduce(
        (acc, value, index) => ({ ...acc, [value]: index + 1 }),
        {}
    )

    // get query params from url /api/team?team=stl&year=2008
    const { team, year } = req.query

    // create a new workbook to write to
    const workbook = new Excel.Workbook()

    // this is important otherwise the path to the file won't resolve correctly since we are in the server
    const dir = path.resolve('./data/team/', team)

    // read in the file that is in csv format
    await workbook.csv.readFile(`${dir}/${year}.csv`)

    // get the first work sheet from the file
    const worksheet = workbook.worksheets[0]

    // gather up each row of data I noticed that the game ids are duplicated by similar stats
    // I didn't look a ton into it but I noticed it might be because of the situation column
    const data = []

    // map over each row and use columnHeaderMap to create row objects
    worksheet.eachRow((row, rowNumber) => {
        const rowObject = Object.entries(columnHeaderMap).reduce(
            (acc, value) => {
                return {
                    ...acc,
                    [value[0]]: row.values[value[1]],
                }
            },
            {}
        )
        // do not add the columns if the spread sheet has the columns in it
        if (rowNumber !== 1) {
            data.push(rowObject)
        }
    })
    // data at this point is all of the rows with the columns
    // const data = [all rows as objects] see comments below for @data data

    // using lodash group by to group all the data by the corresponding gameId
    const groupedByGameId = groupBy(data, 'gameId')

    // using key by to key all of that grouped game id data into keys of the different situations
    // looks like this

    const keyedBySituation = Object.entries(groupedByGameId).map(
        (gameIdData) => ({
            gameId: gameIdData[0],
            ...keyBy(gameIdData[1], 'situation'),
        })
    )

    // since we have duplicate fields we should remove them and make them part of the top level object

    // grab all the keys

    const dataToReturn = keyedBySituation.map((game) => {
        const dataColumnsThatAreTheSame = [
            'team',
            'season',
            'name',
            'gameId',
            'playerTeam',
            'opposingTeam',
            'home_or_away',
            'gameDate',
            'position',
            'situation',
        ]

        console.log(Object.keys(game))
        return Object.keys(game).reduce((acc, key) => {
            if (key === 'gameId') {
                return { ...acc, gameId: game[key] }
            }
            if (key === 'all') {
                const generalData = pick(game[key], dataColumnsThatAreTheSame)

                return {
                    ...acc,
                    ...generalData,
                    [key]: omit(game[key], dataColumnsThatAreTheSame),
                }
            } else {
                return {
                    ...acc,
                    [key]: omit(game[key], dataColumnsThatAreTheSame),
                }
            }
        }, {})
    })
    res.status(200).json({
        teamData: dataToReturn[0],
    })
}

